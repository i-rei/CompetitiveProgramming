r, c = map(int, input().split())
a = [
        [0] * 15,
        [0] + [1] * 13 + [0],
        [0, 1] + [0] * 11 + [1, 0],
        [0, 1, 0] + [1] * 9 + [0, 1, 0],
        [0, 1, 0, 1] + [0] * 7 + [1, 0, 1, 0],
        [0, 1, 0, 1, 0] + [1] * 5 + [0, 1, 0, 1, 0],
        [0, 1, 0, 1, 0, 1] + [0] * 3 + [1, 0, 1, 0, 1, 0]]

b = a[::-1]
m = [0 if i % 2 == 0 else 1 for i in range(15)]
a.append(m)
g = a + b
if g[r-1][c-1] == 0:
    print('black')
else:
    print('white')
